# Take a 3 input from the user
# perform the Add, sub, mul and div

num_1 = int(input('Enter First num : '))
num_2 = int(input('Enter Second num : '))
num_3 = int(input('Enter Third num : '))

Add = num_1 + num_2 + num_3
Sub = num_1 - num_2 -num_3
Mul = num_1 * num_2 * num_3
Div = num_1 / num_2 / num_3

print('Addition of the three numbers : ',Add,
      '\nSubtraction of the three number would be:',Sub,
      '\nMultiplication of the three numbers:',Mul,
      '\nDivision of the three numbers : ',int(Div))

